// ─── Scenario seed selection ──────────────────────────────────────

export function getSeedForLevel(careerKey: string, level: number): ScenarioSeed | null {
  const key = careerKey as CareerKey;
  const pool = SEEDS[key];
  if (!pool || pool.length === 0) return null;

  // Get seeds eligible for this level
  const eligible = pool.filter((s) => level >= s.minLevel && level <= s.maxLevel);

  // If no eligible seeds, use closest level match
  const workingPool = eligible.length > 0 ? eligible : pool;

  // Filter out recently seen seeds (to avoid repetition)
  const recentIds = typeof window !== "undefined" ? getRecentSeedIds() : [];
  const unseen = workingPool.filter((s) => !recentIds.includes(s.id));

  // Pick from unseen seeds if available, otherwise from the full pool
  const selectionPool = unseen.length > 0 ? unseen : workingPool;

  const selected = selectionPool[Math.floor(Math.random() * selectionPool.length)];

  // Track this seed to avoid repetition
  if (typeof window !== "undefined") {
    addRecentSeedId(selected.id);
  }

  return selected;
}

